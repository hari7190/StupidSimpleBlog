# Binary Search - Python

# Prerequisite : Array has to be sorted.

input_arr = [23, 45, 56, 78, 92, 135, 300]

to_find = 56

low = 0

high = len(input_arr)

result = -1

while (low < high):

    mid = low + (high - low)//2

    if(to_find < input_arr[mid]):
        high = mid - 1

    elif(to_find > input_arr[mid]):
        low = mid + 1

    else:
        result = mid
        break

print("Element is at index : " + str(result))


